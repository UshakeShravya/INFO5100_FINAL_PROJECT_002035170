    private void btnAcceptActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcceptActionPerformed
        // TODO add your handling code here:
         int row = tblAcqRequests.getSelectedRow();
    if (row < 0) {
        JOptionPane.showMessageDialog(this, "Select a row first.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // 1) Mark the acquisition request accepted
    MovieAcquisitionRequest r = system.getMovieRequests().get(row);
    r.setStatus("Accepted");

    // 2) Create & register a DistributionAssignmentRequest
    DistributionAssignmentRequest dar =
        new DistributionAssignmentRequest(r.getMovieTitle(), r.getBudget());
    system.addDistributionAssignment(dar);

    // 3) Popup confirmation
    JOptionPane.showMessageDialog(
        this,
        "Acquisition request accepted!",
        "Success",
        JOptionPane.INFORMATION_MESSAGE
    );

    // 4) Refresh YOUR own table
    populateTable();

    // 5) (optional) if you want to immediately jump to the assignments panel, you can do:
    /*
    DistributionAssignmentsPanel dap = new DistributionAssignmentsPanel(workarea, system);
    workarea.add("DistributionAssignments", dap);
    ((CardLayout)workarea.getLayout()).show(workarea, "DistributionAssignments");
    */
    }//GEN-LAST:event_btnAcceptActionPerformed

    private void btnRejectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRejectActionPerformed
        // TODO add your handling code here:
        int row = tblAcqRequests.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Select a row first.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        MovieAcquisitionRequest r = system.getMovieRequests().get(row);
        r.setStatus("Rejected");
        populateTable();
    }//GEN-LAST:event_btnRejectActionPerformed

