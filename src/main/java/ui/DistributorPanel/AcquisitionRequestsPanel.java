    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel)tblAcqRequests.getModel();
        model.setRowCount(0);
        for (MovieAcquisitionRequest r : system.getMovieRequests()) {
            model.addRow(new Object[]{ r.getMovieTitle(), r.getBudget(), r.getStatus() });
        }
    }
