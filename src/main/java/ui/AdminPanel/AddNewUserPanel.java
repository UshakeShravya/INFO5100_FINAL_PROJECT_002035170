    private void btnCreateUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateUserActionPerformed
        // TODO add your handling code here:
       String username = txtUsername.getText().trim();
    String password = txtPassword.getText().trim();
    String name     = txtName.getText().trim();
    String roleStr  = (String) cmbRole.getSelectedItem();
    String orgStr   = (String) cmbOrganization.getSelectedItem();

    // Basic validation
    if (username.isEmpty() || password.isEmpty() || name.isEmpty() || roleStr == null || orgStr == null) {
        JOptionPane.showMessageDialog(this, "All fields must be filled.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Check duplicates
    for (UserAccount ua : system.getUserAccountDirectory().getUserList()) {
        if (ua.getusername().equals(username)) {
            JOptionPane.showMessageDialog(this, "Username already exists.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
    }

    // Map string to actual Role object
    
    Role selectedRole;
    switch (roleStr) {
        case "Producer":         selectedRole = new ProducerRole();        break;
        case "Distributor":      selectedRole = new DistributorRole();     break;
        case "Manager":          selectedRole = new ManagerRole();         break;
        case "Ticket Seller":    selectedRole = new TicketSellerRole();    break;
        case "Customer":         selectedRole = new CustomerRole();        break;
        case "Concession Staff": selectedRole = new ConcessionStaffRole(); break;
        case "Technician":       selectedRole = new TechnicianRole();      break;
        case "Accountant":       selectedRole = new AccountantRole();      break;
        default:
            JOptionPane.showMessageDialog(this, "Unknown role!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
    }

    // Create the UserAccount with a Role instance, not a String
    UserAccount newUser = new UserAccount(
        username,
        password,
        selectedRole,  // <-- correct Role type
        orgStr,
        name
    );
    system.getUserAccountDirectory().addUserAccount(newUser);

    // Confirmation
    JOptionPane.showMessageDialog(this, "User created successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);

    // Go back to ManageUserAccountPanel and refresh its table
    ManageUserAccountPanel panel = new ManageUserAccountPanel(workarea, system);
    workarea.add("ManageUserAccountPanel", panel);
    ((CardLayout) workarea.getLayout()).next(workarea);

    
    }//GEN-LAST:event_btnCreateUserActionPerformed


