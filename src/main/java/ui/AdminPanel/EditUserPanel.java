// Helper to match Role instance to string
private String getRoleString(Role role) {
    if (role instanceof Business.Role.ProducerRole) return "Producer";
    if (role instanceof Business.Role.DistributorRole) return "Distributor";
    if (role instanceof Business.Role.ManagerRole) return "Manager";
    if (role instanceof Business.Role.TicketSellerRole) return "Ticket Seller";
    if (role instanceof Business.Role.CustomerRole) return "Customer";
    if (role instanceof Business.Role.ConcessionStaffRole) return "Concession Staff";
    if (role instanceof Business.Role.TechnicianRole) return "Technician";
    if (role instanceof Business.Role.AccountantRole) return "Accountant";
    return "Unknown";
}

private Role getRoleFromString(String roleName) {
    switch (roleName) {
        case "Producer":
            return new Business.Role.ProducerRole();
        case "Distributor":
            return new Business.Role.DistributorRole();
        case "Manager":
            return new Business.Role.ManagerRole();
        case "Ticket Seller":
            return new Business.Role.TicketSellerRole();
        case "Customer":
            return new Business.Role.CustomerRole();
        case "Concession Staff":
            return new Business.Role.ConcessionStaffRole();
        case "Technician":
            return new Business.Role.TechnicianRole();
        case "Accountant":
            return new Business.Role.AccountantRole();
        default:
            return null;
    }
}


