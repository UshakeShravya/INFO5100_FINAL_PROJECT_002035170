    private void btnAddtoCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddtoCartActionPerformed
        // TODO add your handling code here:
        // Ensure a food item is selected
        if (selectedItem == null || selectedItem.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a food item.");
            return;
        }

        // Ensure quantity is valid
        if (quantity <= 0) {
            JOptionPane.showMessageDialog(this, "Quantity must be greater than zero.");
            return;
        }

        // Calculate price and create item
        int pricePerItem = getPriceForItem(selectedItem);
        int totalPrice = pricePerItem * quantity;

        CartItem item = new CartItem(selectedItem, quantity, totalPrice);

        // Add to user's cart
        user.getCart().add(item);

        JOptionPane.showMessageDialog(this, "Item added to cart: " + selectedItem);
        System.out.println("Cart size now: " + user.getCart().size());
    }//GEN-LAST:event_btnAddtoCartActionPerformed

