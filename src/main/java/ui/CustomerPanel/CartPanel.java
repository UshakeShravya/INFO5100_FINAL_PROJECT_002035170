    private void btnEditQuantityActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQuantityActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            CartItem selectedItem = cart.get(selectedRow);
            String newQtyStr = javax.swing.JOptionPane.showInputDialog(this,
                "Enter new quantity for " + selectedItem.getFoodName() + ":",
                selectedItem.getQuantity());
            if (newQtyStr != null) {
                try {
                    int newQty = Integer.parseInt(newQtyStr);
                    if (newQty <= 0) throw new NumberFormatException();

                    // Recalculate price
                    int pricePerUnit = (int) (selectedItem.getPrice() / selectedItem.getQuantity());
                    selectedItem.setQuantity(newQty);
                    selectedItem.setPrice(pricePerUnit * newQty);

                    populateTable();
                    updateTotal();
                } catch (NumberFormatException e) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Invalid quantity entered.");
                }
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select an item to edit.");
        }
    }//GEN-LAST:event_btnEditQuantityActionPerformed

    private void btnRemoveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveItemActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTable1.getSelectedRow();
        if (selectedRow >= 0) {
            cart.remove(selectedRow);
            populateTable();
            updateTotal();
            javax.swing.JOptionPane.showMessageDialog(this, "Item removed from cart.");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select an item to remove.");
        }
    }//GEN-LAST:event_btnRemoveItemActionPerformed

