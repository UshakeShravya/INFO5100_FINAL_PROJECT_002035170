    private void btnPlaceOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlaceOrderActionPerformed
        // TODO add your handling code here:
        if (cart.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Cart is empty.");
            return;
        }

        // âœ… Create food order request BEFORE clearing the cart
        FoodOrderRequest request = new FoodOrderRequest();
        request.setSender(user);
        request.setStatus("Placed");
        request.setCartItems(new ArrayList<>(cart)); // âœ… make a copy
        request.setRequestDate(new java.util.Date());

        // âœ… Add to global food list
        system.addFoodOrderRequest(request);

        javax.swing.JOptionPane.showMessageDialog(this, "Order placed successfully!");

        populateTable();
        updateTotal();

    }//GEN-LAST:event_btnPlaceOrderActionPerformed

