    public void populateTable() {
        DefaultTableModel model = (DefaultTableModel)tblFinancial.getModel();
        model.setRowCount(0);

        List<FinancialUpdateRequest> list = system.getFinancialRequests();
        for (FinancialUpdateRequest r : list) {
            model.addRow(new Object[]{
                r.getDescription(),
                String.format("%.2f", r.getAmount()),
                r.getStatus()
            });
        }
    }

