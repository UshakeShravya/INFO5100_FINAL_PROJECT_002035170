    private void btnRecordPaymentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRecordPaymentActionPerformed
        // TODO add your handling code here:
         int row = tblFinancial.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Select a row first.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        FinancialUpdateRequest r = system.getFinancialRequests().get(row);
        if (r.getStatus().equals("Paid")) {
            JOptionPane.showMessageDialog(this, "Already marked as Paid.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        r.setStatus("Paid");
        JOptionPane.showMessageDialog(this, "Marked as Paid.", "Success", JOptionPane.INFORMATION_MESSAGE);
        populateTable();
    }//GEN-LAST:event_btnRecordPaymentActionPerformed

