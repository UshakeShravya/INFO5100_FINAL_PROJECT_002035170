    private void populateTable() {
        DefaultTableModel m = (DefaultTableModel)tblProcessDistributionAssignments.getModel();
        m.setRowCount(0);
        for (DistributionAssignmentRequest r : system.getDistributionAssignments()) {
            m.addRow(new Object[]{
                r.getMovieTitle(),
                r.getBudget(),
                r.getStatus(),
                r.getAssignedManager()==null? "<unassigned>": r.getAssignedManager()
            });
        }
    }
    
    private void populateStaffCombo() {
        cmbPickStaff.removeAllItems();
        for (UserAccount ua : system.getUserAccountDirectory().getUserList()) {
            String role = ua.getRole().toString();
            if ( ua.getRole() instanceof TechnicianRole
          || ua.getRole() instanceof ConcessionStaffRole ) {
            cmbPickStaff.addItem(ua.getusername());
            }
        }
    }


