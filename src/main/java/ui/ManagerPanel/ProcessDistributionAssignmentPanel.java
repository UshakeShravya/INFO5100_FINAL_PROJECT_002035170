    private void btnAssigntoStaffActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssigntoStaffActionPerformed
        // TODO add your handling code here:
        int row = tblProcessDistributionAssignments.getSelectedRow();
        if (row<0) {
            JOptionPane.showMessageDialog(this,"Select a row.","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        String staff = (String)cmbPickStaff.getSelectedItem();
        DistributionAssignmentRequest dar = system.getDistributionAssignments().get(row);
        dar.setAssignedManager(staff);
        system.addStaffAssignmentRequest(
          new StaffAssignmentRequest(dar.getMovieTitle(), dar.getBudget(), staff)
        );
        JOptionPane.showMessageDialog(this,"Assigned to "+staff,"Success",JOptionPane.INFORMATION_MESSAGE);
        populateTable();

    }//GEN-LAST:event_btnAssigntoStaffActionPerformed

