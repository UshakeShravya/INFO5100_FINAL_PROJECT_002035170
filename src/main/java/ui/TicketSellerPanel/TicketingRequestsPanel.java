    private void btnIssueTicketsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIssueTicketsActionPerformed
        // TODO add your handling code here:
        int row = tblTicketingRequests.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Select a row first.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        TicketingRequest r = system.getTicketingRequests().get(row);
        if (!r.getStatus().equals("Pending")) {
            JOptionPane.showMessageDialog(this, "Already processed.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        // 1) mark this request “Issued”
        r.setStatus("Issued");

        // 2) create a financial update for the accountant
        //    (e.g. amount = seats * ticket price, here assume $10 each)
        double amount = r.getNumTickets() * 10.0;
        FinancialUpdateRequest finReq = new FinancialUpdateRequest(
            "Issued tickets for " + r.getMovieTitle() 
                + ", customer=" + r.getCustomerName(),
            amount
        );
        system.addFinancialRequest(finReq);

        JOptionPane.showMessageDialog(this, 
            "Tickets issued and Financial update created.", 
            "Success", 
            JOptionPane.INFORMATION_MESSAGE
        );
        populateTable();
    }//GEN-LAST:event_btnIssueTicketsActionPerformed

