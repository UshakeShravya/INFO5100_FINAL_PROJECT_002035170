      private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tblTicketingRequests.getModel();
        model.setRowCount(0);
        List<TicketingRequest> list = system.getTicketingRequests();
        for (TicketingRequest r : list) {
            model.addRow(new Object[] {
                r.getMovieTitle(),
                r.getNumTickets(),
                r.getCustomerName(), 
                r.getStatus()
            });
        }
    }

